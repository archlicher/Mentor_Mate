require_relative 'lib/validation'
require_relative 'lib/draw'

n = ''
validation = Validations.new

until false
	puts 'Please enter a value for N or "x", if you want to exit:'
	n = gets.chomp

	exit if n == 'x'

	n = n.to_i

	break if validation.validate(n)
end

Draw.new.draw(n)

